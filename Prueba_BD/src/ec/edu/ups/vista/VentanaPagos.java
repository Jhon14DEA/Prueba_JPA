    public void cargarDatosTabla(Hipoteca hipoteca) {
        defaultTableModel = (DefaultTableModel) tabla.getModel();
        try {

            Calendar fecha = hipoteca.getFechaInicio();
            int numero = hipoteca.getNumeroPagos() - hipoteca.getPagosFaltantes();
            
            int n = hipoteca.getNumeroPagos();
            defaultTableModel.setRowCount(0);
            int contador = 0;
            String dia = Integer.toString(fecha.get(Calendar.DAY_OF_MONTH));
            String mes = Integer.toString(fecha.get(Calendar.MONTH));
            String a単o = Integer.toString(fecha.get(Calendar.YEAR));
            int m = Integer.valueOf(mes) + 1;
            mes = String.valueOf(m);
            for (int i = 0; i < n; i++) {
                contador += 1;
                String palabra;
                if (contador>numero) {
                    palabra= "Pendiente";
                }else{
                    palabra= "Pagado";
                }
                m = Integer.valueOf(mes) + 1;
                mes = String.valueOf(m);
                if (m > 12) {
                    int a = Integer.valueOf(a単o) + 1;
                    a単o = String.valueOf(a);
                    mes = "1";
                } else {
                }
                String fechaPago = dia + "/" + mes + "/" + a単o;
                String datos[] = {fechaPago, String.valueOf(contador),palabra};
                defaultTableModel.addRow(datos);
            }

        } catch (Exception e) {
        }
    }
