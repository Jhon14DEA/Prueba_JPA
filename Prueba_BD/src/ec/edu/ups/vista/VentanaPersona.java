    public void cargarDatosTabla() {
        defaultTableModel.setRowCount(0);

        for (Persona cliente : controladorPrincipal.getPersonaDAO().getListado()) {
            Calendar fecha = cliente.getFechaNacimiento();
            String dia = Integer.toString(fecha.get(Calendar.DAY_OF_MONTH));
            String mes = Integer.toString(fecha.get(Calendar.MONTH));
            String a単o = Integer.toString(fecha.get(Calendar.YEAR));
            String fechaNacimiento = dia + "/" + mes + "/" + a単o;
            String datos[] = {String.valueOf(cliente.getCodigo()),cliente.getCedula(),cliente.getNombre(),cliente.getApellido(),cliente.getDireccion(),fechaNacimiento,String.valueOf(cliente.getSueldo())};
            defaultTableModel.addRow(datos);
        }

    }

    public void vizualizarDatos(int posicion) {
        if (posicion >= 0) {
            Persona persona = this.controladorPrincipal.getPersonaDAO().getListado().get(posicion);
            Calendar fecha = persona.getFechaNacimiento();
            String dia = Integer.toString(fecha.get(Calendar.DAY_OF_MONTH));
            String mes = Integer.toString(fecha.get(Calendar.MONTH));
            String a単o = Integer.toString(fecha.get(Calendar.YEAR));
            String fechaNacimiento = dia + "/" + mes + "/" + a単o;
            txtCodigo.setText(String.valueOf(persona.getCodigo()));
            txtNombre.setText(persona.getNombre());
            txtCedula.setText(persona.getCedula());
            txtDireccion.setText(persona.getDireccion());
            txtSueldo.setText(String.valueOf(persona.getSueldo()));
            txtApellido.setText(persona.getApellido());
             txtFechaNacimiento.setText(fechaNacimiento);
            txtCedula.setEditable(false);

        } else {
            txtCodigo.setText("");
            txtNombre.setText("");
            txtCedula.setText("");
            txtDireccion.setText("");
            txtSueldo.setText("");
            txtApellido.setText("");
            txtFechaNacimiento.setText("");

        }
    }

