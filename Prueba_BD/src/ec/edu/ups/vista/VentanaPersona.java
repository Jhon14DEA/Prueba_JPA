    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Date fecha1 = formato.parse(txtFechaNacimiento.getText());
            Calendar fecha = new GregorianCalendar();

            fecha.setTime(fecha1);

            try {
                if (txtCodigo.getText().equals("")) {
                    String mensaje = controladorPrincipal.crearPersona(txtCedula.getText(), txtNombre.getText(), txtApellido.getText(), txtDireccion.getText(), fecha, Double.valueOf(txtSueldo.getText()));
                    JOptionPane.showMessageDialog(this, mensaje);

                } else {
                    String mensaje = controladorPrincipal.actualizarPersona(Integer.parseInt(txtCodigo.getText()), txtCedula.getText(), txtNombre.getText(), txtApellido.getText(), txtDireccion.getText(), fecha, Double.valueOf(txtSueldo.getText()));
                    JOptionPane.showMessageDialog(this, mensaje);
                }
            } catch (VacioExcepcion | CedulaExcepcion ex) {
                JOptionPane.showMessageDialog(this, ex.toString());
            } finally {
                cargarDatosTabla();
                this.vizualizarDatos(-1);
            }
        } catch (ParseException ex) {
             JOptionPane.showMessageDialog(this, "Error en la fecha");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

