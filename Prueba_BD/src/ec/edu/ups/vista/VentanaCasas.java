    public void cargarDatosTabla() {
        defaultTableModel.setRowCount(0);

        for (Casa casa : controladorPrincipal.getCasaDAO().getListado()) {
            String estado = "";
            if (casa.getEstado()) {
                estado = "NO";
            } else {
                estado = "SI";
            }
            String datos[] = {String.valueOf(casa.getCodigo()), casa.getDireccion(), String.valueOf(casa.getAvaluo())};
            defaultTableModel.addRow(datos);
        }

    }

    public void vizualizarDatos(int posicion) {
        if (posicion >= 0) {
            Casa casa = this.controladorPrincipal.getCasaDAO().getListado().get(posicion);
            txtCodigo.setText(String.valueOf(casa.getCodigo()));
            txtAvaluo.setText(String.valueOf(casa.getAvaluo()));
            txtDireccion.setText(casa.getDireccion());

        } else {
            txtCodigo.setText("");
            txtAvaluo.setText("");
            txtDireccion.setText("");

        }
    }

