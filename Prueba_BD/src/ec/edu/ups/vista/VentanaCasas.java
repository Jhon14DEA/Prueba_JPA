    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try{
        try {
            if (txtCodigo.getText().equals("")) {
                String mensaje = controladorPrincipal.crearCasa(txtDireccion.getText(), Double.valueOf(txtAvaluo.getText()),false);
                JOptionPane.showMessageDialog(this, mensaje);

            } else {
                String mensaje = controladorPrincipal.actualizarCasa(Integer.valueOf(txtCodigo.getText()), txtDireccion.getText(), Double.valueOf(txtAvaluo.getText()),false);
                JOptionPane.showMessageDialog(this, mensaje);
            }
        } catch (VacioExcepcion ex) {
            JOptionPane.showMessageDialog(this, ex.toString());

        } finally {
            cargarDatosTabla();
            this.vizualizarDatos(-1);
        }
       }catch(Exception ec){
            JOptionPane.showMessageDialog(null, "Error: Datos Mal Ingresados");
    }  
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Casa casa=controladorPrincipal.BuscarCasa(Integer.valueOf(txtCodigo.getText()));
            controladorPrincipal.getCasaDAO().eliminar(casa);
            cargarDatosTabla();
            this.vizualizarDatos(-1);
            JOptionPane.showMessageDialog(this, "Exitoso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

